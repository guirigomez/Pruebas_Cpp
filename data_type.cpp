// Basic data types C++

#include <iostream>

using namespace std;

int main() {

    int num1 = 10;
    float num2 = 3.14;
    double num3 = 2.71828;
    char letter = 'A';

    cout << "Integer data type: " << num1 << endl;
    cout << "Float data type: " << num2 << endl;
    cout << "Double data type: " << num3 << endl;
    cout << "Character data type: " << letter << endl;


    return 0;
}